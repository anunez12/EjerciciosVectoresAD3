    private void cmdSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSumaActionPerformed
        int sumavector; 
        sumavector=Integer.parseInt(txtVectoruno.getText().trim());  
        suma=new double[sumavector]; 
        for (int i = 0; i < a.length; i++) {
            suma[i]=(a[i]+b[i]);
        } 
        for (int i = 0; i < a.length; i++) {
           txtResultado.append("El resultado de la suma en la posicion "+i+" es: "+suma[i]+"\n");
            
        }
        
        
    }//GEN-LAST:event_cmdSumaActionPerformed

