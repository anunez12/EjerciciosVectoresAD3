    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed
     int longitud1,longitud2; 
    if(txtVectoruno.getText().trim().isEmpty()){ 
        JOptionPane.showMessageDialog(this,"Ingrese longitud del vector uno","Error",JOptionPane.ERROR_MESSAGE); 
        txtVectoruno.requestFocusInWindow();
    }else if(txtVectordos.getText().trim().isEmpty()){ 
       JOptionPane.showMessageDialog(this,"Ingrese longitud del vector dos","Error",JOptionPane.ERROR_MESSAGE); 
       txtVectordos.requestFocusInWindow();
    }else if(Integer.parseInt(txtVectoruno.getText().trim())==0){ 
       JOptionPane.showMessageDialog(this,"La longitud del vector no puede ser cero","Error",JOptionPane.ERROR_MESSAGE); 
       txtVectoruno.requestFocusInWindow(); 
       txtVectoruno.selectAll();
    }else if(Integer.parseInt(txtVectordos.getText().trim())==0){ 
       JOptionPane.showMessageDialog(this,"La longitud del vector no puede ser cero","Error",JOptionPane.ERROR_MESSAGE); 
       txtVectordos.requestFocusInWindow(); 
       txtVectordos.selectAll();
    }else{ 
      longitud1=Integer.parseInt(txtVectoruno.getText()); 
      longitud2=Integer.parseInt(txtVectordos.getText()); 
      a=new double[longitud1]; 
      b=new double[longitud2]; 
      JOptionPane.showMessageDialog(this,"Vector creado satisfactoriamente");
    }
    }//GEN-LAST:event_cmdCrearActionPerformed

